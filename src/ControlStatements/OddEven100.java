package ControlStatements;

public class OddEven100 {

	public static void main(String[] args)
	{
		
     int number = 100;		
     System.out.println("List of odd number start from 0 to "+ number);
     for (int a=1; a<=number; a ++) 
     {
     if (a % 2 ==0)
     {
    	 System.out.println(a+	" ");
     }
     
	}
     
}
	
}
