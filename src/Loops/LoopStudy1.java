package Loops;

public class LoopStudy1 {

	public static void main(String[] args) 
	{   
		
		for(int i=1;i<=100;i++)
		{
			System.out.println(i);
		}
		for(int i=2;i<=20;i=i+2)
		{
			System.out.println(i);
		}
		for(int i = 25;i<=250;i=i+25)
		{
			System.out.println(i);
		}
		for (int i=2;i<=100;i=i+2) //even
		{
			System.out.println(i);
		}
		for (int i=3;i<=100;i=i+3) //odd
		{
			System.out.println(i);
		}
	}
	

}
